    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        objEmpleado = new Empleado(Integer.valueOf(lblId.getText()), txt_codigo.getText(), txt_nombres.getText(), txt_apellidos.getText(), txt_direccion.getText(), txt_telefono.getText(), txt_fn.getText());
        if ("Gerente".equals(cmb_puesto.getSelectedItem().toString())) {
            objEmpleado.setIdPuesto(1);
        } else if ("Jefe".equals(cmb_puesto.getSelectedItem().toString())) {
            objEmpleado.setIdPuesto(2);
        } else if ("Asistente".equals(cmb_puesto.getSelectedItem().toString())) {
            objEmpleado.setIdPuesto(3);
        }
        objEmpleado.modificar();
        tblEmpleado.setModel(objEmpleado.leer());
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int opcion = JOptionPane.showConfirmDialog(null, "¿Desea Eliminar el Registro?", "Opcion", JOptionPane.YES_OPTION);
        // yes = 0,no = 1;
        if (opcion == 0) {
            objEmpleado = new Empleado();
            objEmpleado.setId(Integer.valueOf(lblId.getText()));
            objEmpleado.eliminar();
            tblEmpleado.setModel(objEmpleado.leer());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    public void selectDatos() {
        int fila = tblEmpleado.getSelectedRow();
        lblId.setText(tblEmpleado.getValueAt(fila, 0).toString());
        txt_codigo.setText(tblEmpleado.getValueAt(fila, 1).toString());
        txt_nombres.setText(tblEmpleado.getValueAt(fila, 2).toString());
        txt_apellidos.setText(tblEmpleado.getValueAt(fila, 3).toString());
        txt_direccion.setText(tblEmpleado.getValueAt(fila, 4).toString());
        txt_telefono.setText(tblEmpleado.getValueAt(fila, 5).toString());
        txt_fn.setText(tblEmpleado.getValueAt(fila, 6).toString());
    }

