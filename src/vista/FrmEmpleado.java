    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        // TODO add your handling code here:
        /* Fila de la tabla*/
        Empleado objEmpleado = new Empleado();
        objEmpleado.setCodigo(this.txt_codigo.getText());
        objEmpleado.setNombres(this.txt_nombres.getText());
        objEmpleado.setApellidos(this.txt_apellidos.getText());
        objEmpleado.setDireccion(this.txt_direccion.getText());
        objEmpleado.setTelefono(this.txt_telefono.getText());
        objEmpleado.setFechaNacimiento(this.txt_fn.getText());

        if ("Gerente".equals(cmb_puesto.getSelectedItem().toString())) {
            objEmpleado.setIdPuesto(1);
        } else if ("Jefe".equals(cmb_puesto.getSelectedItem().toString())) {
            objEmpleado.setIdPuesto(2);
        } else if ("Asistente".equals(cmb_puesto.getSelectedItem().toString())) {
            objEmpleado.setIdPuesto(3);
        }

        objEmpleado.agregar();
        tblEmpleado.setModel(objEmpleado.leer());
    }//GEN-LAST:event_btn_agregarActionPerformed

